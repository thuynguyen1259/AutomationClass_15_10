package day5_thuchanh;

public abstract class HinhHoc {
	public Double tinhDienTich;

		private String tenHinh;

		public String getTenHinh() {
			return tenHinh;
		}

		public void setTenHinh(String tenHinh) {
			this.tenHinh = tenHinh;
		}
		
		public Double tinhDienTich() {
			return +tinhDienTich();
		}
}
	
